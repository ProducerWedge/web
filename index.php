          <h1>Lawnmower</h1>
            <p>The day began by being torn from my sleep by the sound of my neighbor’s Ferris IS6200 Zero Turn Mower. His name was Frank, he’s what some called an eccentric lunatic. He enjoyed the smell of a Camellia Japonica at dawn and a French machine gun being fired in pitch dark. A large man, with a beard consisting of splintered grey and black hairs, bifocals and always adorned in a bolo tie. The tie had a centerpiece that contained a small compartment that held his middle finger’s distal phalanx, or tip. He lost it in a bowling accident during a late-night high-stakes tournament with the Lucky Strikes, a team of four delinquents that were known to put super glue in their opponent's bowling ball sockets. When Frank bowled the last frame for the win the tip of his middle finger was ripped off as the ball continued down the lane for a strike and the win. Later that night he distinguished his lit lucky strike cigarette in the eye socket of the leader of the Strikes for payback and the actual win.</p>
            <p> A riding lawnmower was overkill due to Frank’s yard being the size of a diorama of a real suburban yard. The first-day inauguration of his mower, three weeks ago, had consisted of him running over a sprinkler head and ripping out a large section of his sprinkler system as it became lodged under the frame. In the confusion, he plummeted off the curb and continued down his cul-de-sac dragging his sprinkler system with him. When he turned back to see the damage, he tumbled off his mower. He hit with a loud thud as his zero-turn aimlessly rolled to the curb and stopped. He laid on his back feeling the warmness of the concrete and stared into the cold blue sky as his past failures and misfortunes flowed abruptly through him. He clutched the end of his bolo tie for strength and angrily pulled himself up.</p>
            <p>Frank was my friend. We were more different than alike. He was the down to my up. I was the shallow comment to his deep thoughts. We met on the highway in a road rage event. I had cut him off without signaling. He had followed me for twenty miles firing chrome-plated ball bearings at my car with his Pro X Slingshot. I finally got tired and just pulled over. We pounded, thrashed, and thumped each other until we grew to like one another. Shaking hands, I realized he was my neighbor.</p>
          <p>I looked out the window to see the mower abruptly stop. Frank angrily banged on the handles. He continued to hit the starter to no avail. I wiped my eyes, grabbed some clothes, and went out to help. As I walked across the street a woman, who appeared to be inside out from her lack of fashion sense, ate a pinwheel sandwich as her Neapolitan Mastiff dropped a load in an unsuspecting yard. She smiled, took another large bite, and then kicked it into the street like Messi kicking a winning goal. I watched her walk away as I entered Frank’s yard.</p>

            He indignantly pulled himself off the mower. We passed each other as I climbed on the seat, and he removed the sparkplug cap. He looked around the neighborhood before unzipping. Frank let it go. I hit the starter. The electric current crackled and popped as it quickly flowed upstream like a run of salmon on their way upriver. The sodium and uric acid delivered the unwanted parcel of electricity quickly and succinctly like an overzealous FedEx driver. The explosive punch to the crotch caused him to scream three octaves higher than normal in pain. He began rolling around the yard. “Tuck and roll. Tuck and roll,” I yelled as the aroma of burning flesh filled our noses.
            I knew my only hope was to run. I leapt off the mower and ran for my house. He followed in a serpentine pattern as he gathered his equilibrium. I ran into my house and slammed the door behind me. Frank rode the closed door into the front hallway as it was ripped from its hinges.
